function createLi(suggestions) {
    var ul = document.getElementById('suggestions');
    ul.style.display = 'block';
    var li = '';
    suggestions.forEach((suggestions, index) => {
        li += '<li id="suggestionList' + index + '">' + suggestions + '</li>';
    });
    ul.innerHTML = li;
    let len = suggestions.length;

    if (len * 28 < 200) {
        ul.style.height = len * 28 + 'px';
    }
    else {
        ul.style.height = '200px';
    }
    if (len > 0) {
        document.getElementById('suggestions').addEventListener('click', suggestionHandler);
        document.getElementById('input_text').addEventListener('keydown', onKeydownHandler);
    }
}

function suggestionHandler(e) {
    document.getElementById('input_text').value = e.target.innerText;
    document.getElementById('suggestions').style.display = 'none';
}

var prev;
var isUp = false;
var isDown = false;

function onKeydownHandler(e) {
    if (e && e.keyCode === 40) {

        if (prev && isDown) {
            selectCountLi += 2
        }

        let sugId = document.getElementById(`suggestionList${selectCountLi}`);
        if (sugId) {
            if (prev) {
                prev.style.background = 'white';
            }
            sugId.style.background = 'red';
            prev = sugId;
            isUp = true;
            isDown = false;
        }
        selectCountLi++;
    }
    else if (e && e.keyCode === 38) {
        if (prev && isUp) {
            selectCountLi -= 2;
        }
        let sugId = document.getElementById(`suggestionList${selectCountLi}`);
        if (sugId) {
            if (prev) {
                prev.style.background = 'white';
            }
            sugId.style.background = 'red';
            prev = sugId;
            isDown = true;
            isUp = false;
        }
        selectCountLi--
    }

}

